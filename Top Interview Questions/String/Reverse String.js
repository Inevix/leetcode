/**
 * @param {character[]} s
 * @return {void} Do not return anything, modify s in-place instead.
 */
var reverseString = function (s) {
    return s.reverse();
};

console.log(reverseString(['h', 'e', 'l', 'l', 'o'])); // ['o', 'l', 'l', 'e', 'h']
console.log(reverseString(['H', 'a', 'n', 'n', 'a', 'h'])); // ['h', 'a', 'n', 'n', 'a', 'H']
